    private void bBuscarArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBuscarArchivoActionPerformed

        JFileChooser selectorArchivos = new JFileChooser();
        selectorArchivos.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        int resultado = selectorArchivos.showOpenDialog(this);
        if ((selectorArchivos.getSelectedFile() == null) || (selectorArchivos.getSelectedFile().getName().equals(""))) {
            textRuta.setText("");
        } else {
            ruta = selectorArchivos.getSelectedFile().getAbsolutePath();
            textRuta.setText(ruta);
        }
    }//GEN-LAST:event_bBuscarArchivoActionPerformed

    /**
     *
     * @param evt
     */
    private void bCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCalcularActionPerformed
        bCalcular.setEnabled(false);
        try {
            control = new Control(ruta);
            mostrarResultados(control);
        }catch(FileNotFoundException fnfe){
            JOptionPane.showMessageDialog(null, fnfe.getMessage(), "ERROR", 0);
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, nfe.getMessage(), "ERROR", 0);
        }
    }//GEN-LAST:event_bCalcularActionPerformed
