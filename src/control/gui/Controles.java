    /**
     *
     * @param control
     */
    private void mostrarResultados(Control control) {

        labelPromedio.setText(String.format("%.2f", control.getPromedio()));
        labelDesvEstandar.setText(String.format("%.2f", control.getDesvEstandar()));
    }

